	query_base_url = "https://exoplanetarchive.ipac.caltech.edu/TAP/sync?query="
	query_table = "ps"
	url_to_download_all_data = make_tap_query_url(query_base_url,query_table)
end

# ╔═╡ 0d45aa80-de74-4805-b38f-571007a31e0f
md"""

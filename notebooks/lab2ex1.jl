# ╔═╡ dfd5870a-d3fd-43cf-afef-a46972093e30
md"""
The Exoplanet Archive (and many other astronomical databases) can be queried using the *Table Access Protocol (TAP)* which is maintained by the International Virtual Observatory Alliance (IVOA).  
If we just wanted to download every column and every row from the *table* named `ps`, we could request that using the url
`https://exoplanetarchive.ipac.caltech.edu/TAP/sync?query=select+*+from+ps&format=tsv`.  
I've provided a simple function, `make_tap_query_url` in `src/Lab2.jl` that makes it a little easier to build queries by specifying a base url and the table name (with the option to add a few commands that you'll see soon). 
"""

# ╔═╡ 4be2fe02-bc08-47bd-a852-9c0d64a23c95
